package com.MTlovec.Phone_Store.security;

import com.MTlovec.Phone_Store.model.LOGIN_TYPE;
import com.MTlovec.Phone_Store.model.User;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;

public class CustomUserDetail implements UserDetails {

    private  final User user;

    public CustomUserDetail(User user) {
        this.user = user;
    }

    public LOGIN_TYPE loginType(){
        return user.getType();
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return List.of(new SimpleGrantedAuthority(user.getRole().toString()));
    }

    @Override
    public String getPassword() {
        if (user.getType()==LOGIN_TYPE.GOOGLE){
            return "";
        }
        else return user.getPassword();
    }

    @Override
    public String getUsername() {
        return user.getEmail();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
